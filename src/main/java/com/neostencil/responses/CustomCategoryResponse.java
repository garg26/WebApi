package com.neostencil.responses;

public class CustomCategoryResponse {

  String name;
  boolean havingSubjects;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public boolean isHavingSubjects() {
    return havingSubjects;
  }

  public void setHavingSubjects(boolean havingSubjects) {
    this.havingSubjects = havingSubjects;
  }
}
